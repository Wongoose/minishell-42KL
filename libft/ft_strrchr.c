#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	size_t	i;

	i = ft_strlen(s);
	s += i;
	while (*s != (char)c)
	{
		if (i == 0)
			return (0);
		s--;
		i--;
	}
	return ((char *)s);
}
